#include<stdio.h>

int main()
{
	int i;
	scanf("%d", &i);
	if (i <= 10000)
	{
		i = i * 0.1;
	}
	else if (i > 10000 && i <= 20000)
	{
		i = 10000 * 0.1 + (i - 10000) * 0.075;
	}
	else if (i > 20000 && i <= 40000)
	{
		i = 10000 * 0.175 + (i - 20000) * 0.05;
	}
	else if (i > 40000 && i <= 60000)
	{
		i = 10000 * 0.175 + 20000 * 0.05 + (i - 40000) * 0.03;
	}
	else if (i > 60000 && i <= 100000)
	{
		i = 10000 * 0.175 + 20000 * 0.08 + (i - 60000) * 0.015;
	}
	else
	{
		i = 10000 * 0.175 + 20000 * 0.08 + 40000 * 0.015+(i-100000)*0.01;
	}
	printf("%d\n", i);
	return 0;
}
